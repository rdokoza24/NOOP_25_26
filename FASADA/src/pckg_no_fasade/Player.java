package pckg_no_fasade;

public class Player {

    public void turnOnPlayer() {
        System.out.println("Player is turned ON");
    }

    public void turnOffPlayer() {
        System.out.println("Player is turned OFF");
    }

    public void feedForward(){
        System.out.println("Player is feeding forward");
    }

    public void rewind(){
        System.out.println("Player is rewinding");
    }

    public void pause(){
        System.out.println("Player is paused");
    }
}
